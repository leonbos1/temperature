export default {
  name: "App",
  data: function () {
    return {
      loggedIn: false,
      url: datajson["url"],
    };
  },
  mounted() {
    this.checkLogin();
  },
  methods: {
    checkLogin() {
      fetch(this.url + "/checklogin", {
        method: "GET",
        headers: { token: localStorage.getItem("token") },
      }).then((data) => {
        if (data.status == 200) {
          this.loggedIn = true;
        } else {
          this.loggedIn = false;
        }
      });
    },
    logout() {
      localStorage.removeItem("token");
      this.loggedIn = false;
      window.location.href = "/";
    },
  },
};
